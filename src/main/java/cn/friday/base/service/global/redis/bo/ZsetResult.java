package cn.friday.base.service.global.redis.bo;
/**
 * zset返回的结果解析
 * @author Zz
 *
 */
public class ZsetResult {
	
	public ZsetResult(String value, Double score) {
		super();
		this.value = value;
		this.score = score;
	}

	private String value;
	
	private Double score;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public Double getScore() {
		return score;
	}

	public void setScore(Double score) {
		this.score = score;
	}

	@Override
	public String toString() {
		return "ZsetResult [value=" + value + ", score=" + score.longValue() + "]";
	}

}
